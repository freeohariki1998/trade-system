import { Candle } from "../../../types/candle";

/**
 * ブレイクアウトの「質」を 0〜5 点でスコア化する関数。
 * 直近2本のローソク足だけを使って、
 * ・高値ブレイクの強さ
 * ・出来高の増加
 * ・ローソク足の方向性
 * を評価する。
 *
 * トレードロジックでは
 * 「初動の強さ」「出来高の裏付け」「陽線で終わっているか」
 * がブレイク成功率に直結するため、この3点を重視している。
 */
export function computeBreakoutQuality(candles: Candle[]): number {
  // ローソク足が2本未満なら評価不能 → 最低スコア1
  if (candles.length < 2) return 1;

  // 直近2本のローソク足を取得
  const last = candles[candles.length - 1];     // 最新のローソク足（ブレイク足）
  const prev = candles[candles.length - 2];     // その1本前（基準足）

  // 念のため安全チェック
  if (!last || !prev) return 1;

  let score = 0;

  // ---------------------------------------------------------
  // 高値ブレイクしているか（最重要）
  // ---------------------------------------------------------
  // 「最新の close が 1本前の high を上抜けている」
  // → 明確なブレイクアウト
  if (last.close_price > prev.high_price) score += 2;

  // ---------------------------------------------------------
  // 出来高が急増しているか（勢いの裏付け）
  // ---------------------------------------------------------
  // 出来高が 30% 以上増えている → 本物のブレイクの可能性が高い
  if (last.volume > prev.volume * 1.3) score += 2;

  // ---------------------------------------------------------
  // 陽線で終わっているか（買い圧力の継続）
  // ---------------------------------------------------------
  // close > open → 買いが勝っている
  if (last.close_price > last.open_price) score += 1;

  // 最大スコアは 5
  return Math.min(score, 5);
}
