import { Candle } from "../../../types/candle";

/**
 * チャートが「チョッピー（ノイズが多い・汚い）」かどうかを判定する関数。
 *
 * ロジックでは、
 * ・ギャップが多い
 * ・ローソク足同士のつながりが悪い
 * ・滑らかに推移していない
 * こういうチャートは負けやすいと判断する。
 *
 * この関数は「ローソク足のつながりの悪さ」を
 * ・前足の終値と次足の始値のギャップ
 * ・そのギャップが 0.3% を超える回数
 * で評価し、2 回以上あれば “チョッピー” と判定する。
 */
export function computeChoppy(candles: Candle[]): boolean {
  // 最低 5 本は必要（短すぎるとノイズ判定ができない）
  if (candles.length < 5) return false;

  let gaps = 0;

  // ---------------------------------------------------------
  // 各ローソク足の「前足 close → 次足 open のギャップ」を調べる
  // ---------------------------------------------------------
  for (let i = 1; i < candles.length; i++) {
    const prev = candles[i - 1];
    const cur = candles[i];

    // TS の安全ガード（undefined 回避）
    if (!prev || !cur) continue;

    // ギャップ = |次足の始値 - 前足の終値|
    const gap = Math.abs(cur.open_price - prev.close_price);

    // ---------------------------------------------------------
    // ギャップが 0.3% を超えていたらノイズとカウント
    // ---------------------------------------------------------
    // 0.3% は「滑らかさが失われるライン」としてあなたが設定した閾値
    if (gap > prev.close_price * 0.003) gaps++;
  }

  // ---------------------------------------------------------
  // ギャップが 2 回以上 → チャートが汚い（チョッピー）
  // ---------------------------------------------------------
  return gaps >= 2;
}
