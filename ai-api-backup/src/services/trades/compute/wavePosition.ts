import { Candle } from "../../../types/candle";

/**
 * 波の位置（初動 / 2段目 / 3段目）を判定する関数。
 *
 * トレードでは、
 * ・初動（1段目）は勝ちやすい
 * ・2段目はまだOK
 * ・3段目は危険（高値掴みになりやすい）
 * という明確な傾向があるため、
 * 「直近の高値に対してどの位置にいるか」を数値化している。
 *
 * 判定基準：
 *   last >= max * 0.98 → 2段目（直近高値付近）
 *   last >= max * 0.90 → 3段目（高値圏）
 *   それ以外 → 初動（1段目）
 */
export function computeWavePosition(candles: Candle[]): number {
  // データが少ない場合は初動扱い
  if (candles.length < 5) return 1;

  // 各ローソク足の高値だけを抽出
  const highs = candles.map(c => c.high_price);
  if (highs.length < 5) return 1;

  // 過去5本の中での最高値
  const max = Math.max(...highs);

  // 最新ローソク足の高値
  const last = highs[highs.length - 1]!;

  // ---------------------------------------------------------
  // ① last が最高値の 98% 以上 → 2段目
  // ---------------------------------------------------------
  // 直近高値にほぼ並んでいる → まだ勢いがある位置
  if (last >= max * 0.98) return 2;

  // ---------------------------------------------------------
  // ② last が最高値の 90% 以上 → 3段目
  // ---------------------------------------------------------
  // 高値圏に突入している → リスクが高い
  if (last >= max * 0.90) return 3;

  // ---------------------------------------------------------
  // ③ それ以外 → 初動（1段目）
  // ---------------------------------------------------------
  // 高値から十分離れている → 伸びしろがある
  return 1;
}
